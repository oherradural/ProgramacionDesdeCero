# declaramos las variables
# utilizamos input para que se guarde la informacion

num1=input("Ingresa un numero :")
num2=input("Ingresa otro numero :")
# realizamos la suma y guardamos el resultado
resultado1 = float(num1) + float(num2)
# realizamos la resta y guardamos el resultado
resultado2 = float(num1) - float(num2)
# realizamos la multiplicacion y guardamos el resultado
resultado3 = float(num1) * float(num2)
# realizamos la division y guardamos el resultado
resultado4 = float(num1) / float(num2)
# realizamos la potencia y guardamos el resultado
resultado5 = float(num1) ** float(num2)
print("La suma es", resultado1)
print("La resta es", resultado2)
print("La multiplicacion es", resultado3)
print("La division es:", resultado4)
print ("La potencia es:", resultado5)
